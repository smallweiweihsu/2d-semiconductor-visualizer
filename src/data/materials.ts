import type {
  DiffusionParameter,
  Material,
  MaterialParameter,
  MaterialParameters,
  ParameterConfidence,
} from '../types/material'

const unknownNote = '需要文獻參數'
const estimatedNote = '常見近似值；實際值會受樣品、晶向、厚度、表面狀態與製程影響'

function parameter(
  value: number | string | null,
  unit: string | undefined,
  confidence: ParameterConfidence,
  note?: string,
): MaterialParameter {
  return {
    value,
    unit,
    confidence,
    note: note ?? (confidence === 'unknown' ? unknownNote : estimatedNote),
  }
}

function unknown(unit?: string, note = unknownNote): MaterialParameter {
  return parameter(null, unit, 'unknown', note)
}

function estimated(
  value: number | string,
  unit?: string,
  note = estimatedNote,
): MaterialParameter {
  return parameter(value, unit, 'estimated', note)
}

function known(value: number | string, unit?: string, note?: string) {
  return parameter(value, unit, 'known', note)
}

function createParameters(
  overrides: Partial<MaterialParameters> = {},
): MaterialParameters {
  return {
    workFunction_eV: unknown('eV', '需要文獻參數；會受到表面狀態與製程影響'),
    bandGap_eV: unknown('eV'),
    electronAffinity_eV: unknown('eV'),
    dielectricConstant: unknown(undefined),
    mobility_cm2Vs: unknown('cm²/V·s'),
    resistivity_ohm_m: unknown('Ω·m'),
    latticeConstant_A: unknown('Å'),
    defaultThickness_nm: unknown('nm'),
    breakdownField_MVcm: unknown('MV/cm'),
    meltingPoint_C: unknown('°C'),
    ...overrides,
  }
}

function createDiffusion(
  confidence: ParameterConfidence = 'unknown',
  notes = '需要文獻參數；擴散會受到溫度、界面、晶粒、缺陷與退火環境影響。',
): DiffusionParameter {
  return {
    D0_m2s: unknown('m²/s', '需要文獻參數；不可用預設值進行定量擴散模擬'),
    Ea_eV: unknown('eV', '需要文獻參數；不可用預設值進行定量擴散模擬'),
    confidence,
    notes,
  }
}

