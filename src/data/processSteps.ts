function unknownParameter(
  id: string,
  label_zh: string,
  unit?: string,
  note_zh = '需要使用者輸入或文獻參數；目前僅作為製程流程資料欄位。',
): ProcessParameter {
  return {
    id,
    label_zh,
    value: null,
    unit,
    confidence: 'unknown',
    note_zh,
  }
}

